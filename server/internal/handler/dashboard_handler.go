// UpdateDashboard 更新大屏
func (h *DashboardHandler) UpdateDashboard(w http.ResponseWriter, r *http.Request) {
	// PUT /api/tenants/{id}/dashboards/{dashboardId}
	path := strings.TrimPrefix(r.URL.Path, "/api/tenants/")
	parts := strings.Split(strings.Trim(path, "/"), "/")

	if len(parts) < 3 {
		http.Error(w, "Invalid path", http.StatusBadRequest)
		return
	}

	tenantID := parts[0]
	dashboardID := parts[2]

	dashboard, err := h.service.GetDashboard(dashboardID, tenantID)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	var req struct {
		Name        string `json:"name"`
		Description string `json:"description"`
		LayoutType  string `json:"layoutType"`
		Widgets     string `json:"widgets"`
		StoryOrder  string `json:"storyOrder"`
		Theme       string `json:"theme"`
	}

	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, err.Error(), http.StatusBadRequest)
		return
	}

	if req.Name != "" {
		dashboard.Name = req.Name
	}
	if req.Description != "" {
		dashboard.Description = req.Description
	}
	if req.LayoutType != "" {
		dashboard.LayoutType = model.DashboardLayoutType(req.LayoutType)
	}
	if req.Widgets != "" {
		dashboard.Widgets = req.Widgets
	}
	if req.StoryOrder != "" {
		dashboard.StoryOrder = req.StoryOrder
	}
	if req.Theme != "" {
		dashboard.Theme = req.Theme
	}

	if err := h.service.UpdateDashboard(dashboard); err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(dashboard)
}

