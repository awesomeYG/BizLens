// DashboardHandler 大屏处理器
type DashboardHandler struct {
	service *service.DashboardService
}

// NewDashboardHandler 创建大屏处理器
func NewDashboardHandler(service *service.DashboardService) *DashboardHandler {
	return &DashboardHandler{service: service}
}

// ListDashboards 获取大屏列表
func (h *DashboardHandler) ListDashboards(w http.ResponseWriter, r *http.Request) {
	// /api/tenants/{id}/dashboards[?dataSourceId=xxx]
	path := strings.TrimPrefix(r.URL.Path, "/api/tenants/")
	parts := strings.Split(strings.Trim(path, "/"), "/")

	if len(parts) < 2 {
		http.Error(w, "Invalid path", http.StatusBadRequest)
		return
	}

	tenantID := parts[0]
	dataSourceID := r.URL.Query().Get("dataSourceId")

	dashboards, err := h.service.ListDashboards(tenantID, dataSourceID)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(dashboards)
}

